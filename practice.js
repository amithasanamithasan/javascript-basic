// difference between while loop and for loop
// For Loop:
// A for loop is typically used when you know in advance 
// how many times you want to iterate.
// It has a clear and compact syntax

// for (initialization; condition; increment/decrement) {
//     // Code to be executed in each iteration
//   }

// Initialization: It's where you initialize a counter variable.
// Condition: It's a condition that, if true, allows the loop to continue.
// Increment/Decrement: It updates the counter variable in each iteration.


// for (let i = 0; i < 5; i++) {
//     console.log(i);
//   }
//   This for loop will print numbers from 0 to 4.


// While Loop:
// A while loop is used when you don't know in advance how many times you want to iterate, and the loop continues as long as a specified condition is true.

// javascript
// Copy code
// while (condition) {
//   // Code to be executed in each iteration
// }
// Example:

// javascript
// Copy code
// let i = 0;
// while (i < 5) {
//   console.log(i);
//   i++;
// }
// This while loop will also print numbers from 0 to 4, 
// but it requires manual initialization and updating of the loop control 
// variable (i in this case).

// In summary, use a for loop 
// when you have a clear idea of the number of 
// iterations, and a while loop when the number of 
// iterations depends on a certain condition. 
// Both loops have their own strengths and use cases, 
// and you can choose the one that best fits your specific scenario.





